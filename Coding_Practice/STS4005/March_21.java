//Priorityqueue has four sections
import java.util.*;
public class March_21 {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int m=sc.nextInt();
        int n=sc.nextInt();
        int marr[]=new int[m];
        int narr[]=new int[n];
        for(int i=0;i<m;i++){
            marr[i]=sc.nextInt();
        }
        for(int j=0;j<n;j++){
            narr[j]=sc.nextInt();
        }
        
    }
}
